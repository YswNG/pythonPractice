def format_sql(l=[]):

	s = "insert into m_sys_dict (dictName,dictkey,DictValue,orderno,flg) values "

	for i in range(0,len(l)):
		s += "('City',{0},\'{1}\',{2},1),".format(l[i]["key"],l[i]["value"],i+1)
	# print(s)

	with open('result.txt','w',encoding='utf-8') as f:
		f.write(s[:len(s)-1])


